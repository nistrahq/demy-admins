package com.nistra.demy.admins.core.ui.preview

/**
 * Composite annotation to apply both light and dark tablet previews.
 *
 * Use this annotation on a @Composable function to generate previews
 * for both light and dark themes on tablet devices.
 *
 * @author Salim Ramirez
 */
@TabletPreviewLight
@TabletPreviewDark
annotation class TabletPreviewBoth
